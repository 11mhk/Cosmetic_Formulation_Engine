def explain_ingredients(ingredients_str: str, attributes: dict) -> str:
    ingredients = [i.strip() for i in ingredients_str.split(",")]
    
    lines = ["Verified Cosmetic Ingredients (With Trusted Sources) 🧴", ""]
    
    for ing in ingredients:
        ing_lower = ing.lower()
        data = INGREDIENT_DATA.get(ing_lower)
        
        if data:
            emoji = data["emoji"]
            name = ing.title()
            lines.append(f"{name} {emoji}")
            
            for source in data["sources"]:
                lines.append(f"        - {source}")
            
            lines.append("    > Verification:")
            for v in data["verification"]:
                lines.append(f"        - {v}")
            
            lines.append("    ✔ Proven Benefits:")
            for b in data["benefits"]:
                lines.append(f"        - {b}")
            
            if data["note"]:
                lines.append(f"⚠️ Note: {data['note']}")
        else:
            lines.append(f"{ing.title()}🔹")
            lines.append("    > Proven Benefits:")
            lines.append("        - Supports overall lip health")
        
        lines.append("")
    
    return "\n".join(lines).strip()


